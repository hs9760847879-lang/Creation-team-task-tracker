function parseMainSheet(csv) {
  const lines = csv.trim().split('\n')
  const data = []
  for (let i = 1; i < lines.length; i++) {
    const cols = lines[i].split(',')
    if (!cols[2]) continue
    const date = cols[2].trim()
    if (date === 'Date' || !date) continue
    // Only include dates through 2026-07-04 (today)
    if (date > '2026-07-04') continue
    // Skip if date is a Sunday/weekend with all zeros (template rows)
    const day = cols[6]?.trim() || ''
    data.push({
      date,
      total_commission_updated: parseInt(cols[0]) || 0,
      total_commission_created: parseInt(cols[1]) || 0,
      number_of_mails_assigned: parseInt(cols[3]) || 0,
      number_of_properties_created: parseInt(cols[4]) || 0,
      number_of_faqs_updated: parseInt(cols[5]) || 0,
      day_of_week: day,
      total_properties_api_enabled: cols[7] ? parseInt(cols[7]) || 0 : 0,
      video_created: cols[8] ? parseInt(cols[8]) || 0 : 0,
      stagging_property_creation: cols[9] ? parseInt(cols[9]) || 0 : 0,
    })
  }
  return data
}

function parseAgentSheet(csv) {
  const lines = csv.trim().split('\n')
  const data = []

  // Row 0 (index 0): "Date,Total Commission Updated,,,,,...."
  // Row 1 (index 1): ",Himanshu,Sanket,...,,Himanshu,..." (agent names row)
  // Data starts from index 2

  for (let i = 2; i < lines.length; i++) {
    const cols = lines[i].split(',')
    const date = cols[0]?.trim()
    if (!date || date > '2026-07-04') continue
    // Skip empty/Sunday template rows
    const hasData = cols.slice(1, 28).some(c => c && c.trim() && parseInt(c.trim()) !== 0)
    if (!hasData) continue

    // Columns 1-8 (indices 1-8): Total Commission Updated for 8 agents
    // Column 9: empty separator
    // Columns 10-17: Total Commission Created for 8 agents
    // Column 18: empty separator
    // Columns 19-26: Total Properties Created for 8 agents

    for (let a = 0; a < AGENTS.length; a++) {
      const commUpdated = parseInt(cols[1 + a]) || 0
      const commCreated = parseInt(cols[10 + a]) || 0
      const propsCreated = parseInt(cols[19 + a]) || 0

      if (commUpdated === 0 && commCreated === 0 && propsCreated === 0) continue

      data.push({
        date,
        agent_name: AGENTS[a],
        commission_updated: commUpdated,
        commission_created: commCreated,
        properties_created: propsCreated,
      })
    }
  }
  return data
}

